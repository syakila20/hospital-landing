function randomSchedule(i: number) {
  const count = 2 + (i % 4);
  return Array.from({ length: count }, (_, idx) => {
    const startHour = 8 + (idx % 5);
    const endHour = 12 + (idx % 5);

    return {
      day: days[(i + idx) % 7],
      start: `${startHour.toString().padStart(2, "0")}:00`,
      end: `${endHour.toString().padStart(2, "0")}:00`,
    };
  });
}

